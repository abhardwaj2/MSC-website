    <h1 class="content-main-heading"><em>Intensive Journal</em> Method: A Form of Prayer</h1>
    <p>The format of the <em>Intensive Journal</em> workbook and its individual exercises, and the workshop atmosphere are some of the reasons that the <em>Intensive Journal</em> method has been viewed as a form of prayer </p>

    <p id="subheading">Prayer and meditation in the midst of life:</p>
    <p>Dr. Progoff states that the</p>
    <p>"<em>Intensive Journal</em> work is indeed a type of prayer and meditation but not in isolation from life and not in place of active life involvement. Rather, it is meditation in the midst of the reality of our life experiences. It draws upon the actualities of life for new awarenesses, and it feeds these back into the movement of each life as a whole."<sup>1</sup></p>

    <p id="subheading">Atmosphere for prayer:</p>
    <p>The inward movement of the method creates an inherently meditative quality where the workshops have reminded participants "of profound prayer or of deep meditation." (AJW p 8) Prayer requires a "special condition of being, an inner state of unity, freed from the interventions of his [her] conscious ego, in which the ground of his [her] being was connected with an at-one with the abiding reality of God."<sup>2</sup></p>

    <p id="subheading">Being in relationship with God:</p>
    <p>The essence of prayer is being in relationship with God. A key aspect of the <em>Intensive Journal</em> method is being in relationship with major aspects of your life, including wisdom figures such as God. These exercises help you gain unpredictable insights as you deepen your connection to each area. </p>

    <p id="subheading">Adopting an accepting and non-judgmental approach:</p>
    <p>Effective prayer requires each of us to bare our soul to God to accept divine wisdom and forgiveness. We work in the <em>Intensive Journal</em> method using a non-judgmental approach, recording both positive and negative thoughts as a way to honestly connect with the full scope of our lives. </p>

    <p id="subheading">Repetitive meditative breathing exercises:</p>
    <p>The <em>Intensive Journal</em> method uses a repetitive meditative breathing exercise that is called the Mantra/Crystal exercise, to create a great energy and atmosphere for wisdom. </p>


