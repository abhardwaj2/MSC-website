    <h2>Muddy/Clear: The Mirror of the Water</h2>
    <h2>From The Well and the Cathedral: An Entrance Meditation Reading,</h2>
    <h2>by Ira Progoff, Ph.D.</h2>

    <div class="samplereading">

        <br>

        <p data-verse="1.">We are sitting</p>
        <p>In a place of quietness</p>
        <p>Letting the Self become still,</p>
        <p>Letting the breath become slow,</p>
        <p>Letting our thoughts come to rest.</p>

        <br>

        <p data-verse="2.">Letting the Self become still,</p>
        <p>Energies that were moving about</p>
        <p>Can go inward now,</p>
        <p>Can come to rest</p>
        <p>In the stillness</p>
        <p>Of our quiet being.</p>

        <br>

        <p data-verse="3.">Breathing becomes quiet now,</p>
        <p>Not breathing</p>
        <p>By the tempo of outer things</p>
        <p>But by an inner tempo</p>
        <p>Breathing at an inner pace,</p>
        <p>The breath moving in</p>
        <p>And out</p>
        <p>Of itself,</p>
        <p>Carried by its own rhythm</p>
        <p>Adjusting itself</p>
        <p>To itself.</p>

        <br>

        <p data-verse="4.">Breathing at an inner pace,</p>
        <p>Our thoughts let go</p>
        <p>Of our breathing.</p>
        <p>Breathing at an inner pace,</p>
        <p>The breath is free</p>
        <p>To come and go</p>
        <p>In its own timing.</p>
        <p>The breath is slow</p>
        <p>And regular,</p>
        <p>Moving in and out</p>
        <p>By its inner tempo,</p>
        <p>Carried by its own rhythm,</p>
        <p>Adjusting itself</p>
        <p>To itself.</p>

        <br>

        <p data-verse="5.">Breathing at an inner pace</p>
        <p>Thoughts become quiet,</p>
        <p>Restless thoughts</p>
        <p>That have been moving about,</p>
        <p>Restless thoughts</p>
        <p>Dissipating their energies together</p>
        <p>Into one place</p>
        <p>Resting</p>
        <p>On the steady breathing.</p>

        <br>

        <p data-verse="6.">Excess thoughts drop away.</p>
        <p>We become still.</p>
        <p>Thinking becomes quiet,</p>
        <p>Thoughts fitting together</p>
        <p>And settling into one place</p>
        <p>By themselves</p>
        <p>Without our thinking them.</p>
        <p>Many mixed thoughts</p>
        <p>Become one whole thought</p>
        <p>Contained within itself,</p>
        <p>One whole thought</p>
        <p>In the mind at rest.</p>

        <br>

        <p data-verse="7.">Letting the Self become still,</p>
        <p>Letting our thoughts come to rest,</p>
        <p>Letting our breath become slow.</p>
        <p>Breathing becomes quiet,</p>
        <p>Breathing becomes slow,</p>
        <p>And slower;</p>
        <p>Breathing becomes regular,</p>
        <p>Regular.</p>
        <p>The unevenness</p>
        <p>Of nonessential thoughts</p>
        <p>Drops out of the breathing.</p>
        <p>It becomes</p>
        <p>The breathing of the Self.</p>

        <br>

        <p data-verse="8.">Breathing at an inner pace,</p>
        <p>The breath moves</p>
        <p>At the center of my Self,</p>
        <p>At the center of my Self</p>
        <p>In regular rhythms.</p>
        <p>My body is quiet,</p>
        <p>Holding its place.</p>
        <p>The breath is moving evenly,</p>
        <p>Inward,</p>
        <p>Outward,</p>
        <p>Evenly</p>
        <p>In its own rhythm.</p>
        <p>The breath moves evenly</p>
        <p>At the center of my body,</p>
        <p>At the center of my Self.</p>

        <br>

        <p data-verse="9.">The breath is moving</p>
        <p>At the center of my Self</p>
        <p>In a regular rhythm.</p>
        <p>The breath moves at the center.</p>
        <p>The breath moves at the center</p>
        <p>Breathing at an inner pace.</p>
        <p>As the breath moves at the center,</p>
        <p>Quietly,</p>
        <p>Evenly,</p>
        <p>The Self becomes still</p>
        <p>Like quiet water.</p>

        <br>

        <p data-verse="10.">The Self becomes still</p>
        <p>Like quiet water.</p>
        <p>In the stillness of the Self,</p>
        <p>In the quiet of the water</p>
        <p>My inward ear hears,</p>
        <p>My inward eye sees</p>
        <p>Signs and words and visions</p>
        <p>Reflected in the quiet waters</p>
        <p>In the stillness of the Self,</p>
        <p>In the Silence... In the Silence.</p>
