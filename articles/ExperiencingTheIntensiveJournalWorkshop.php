    <p id="subheading">Experiencing the Intensive Journal™ Workshops</p>
    <p>By Elsa S Bakkum*</p>

    <p><em>Elsa S Bakkum serves as Education for Ministry Associate Director for Training. She has been involved with EfM since 1992 as a Mentor, Online mentor, Coordinator, and Trainer.</em></p>

    <p>The <em>Intensive Journal</em> process and Education for Ministry (EfM) complement one another exceptionally well. I attended the three core workshop modules of the Intensive Journal process in May, 2017. </p>

    <p>In both programs, the present moment is a focus for reflection which radiates with a sense of reality that is ripe for exploration. Each program radically trusts that all of life and value are present in the moment and enriched by dwelling with experiences and the beliefs, connections and implications of the beginning point.</p>

    <p>There is genius built into the Intensive Journal process that moves between concrete life experience and hidden levels of the inner processes of human functioning, making the bigger picture—which often seems to elude awareness—actually become accessible. The Intensive Journal workbook itself strikes me as a brilliant system for guiding individual awareness and reflection on the various components of life that give an individual distance enough to witness movement toward fulfillment. The language is accessible to those in different walks of life and across religious differences.</p>

    <p>EfM folks will recognize many compatible approaches:</p>

    <ul>
        <li>The Steppingstone format to grasp and capture your life story - The Steppingstone model of the Intensive Journal method was adopted long ago in EfM for the Spiritual Autobiography process;</li>
        <li>Engaging the power and depth of images/metaphors;</li>
        <li>Following the energy that arises during reflection;</li>
        <li>Trusting a wise, guided process;</li>
        <li>Looking back in order to look forward.</li>
    </ul>

    <p>I found the <em>Intensive Journal</em> workshops to be rich contexts in which to enter silence, encircled by the silence created by the other participants doing their own depth work. The facilitator was a gentle voice urging me simply to acknowledge the ‘is-ness’ or reality of my life experiences and impressions. I came away feeling centered within my spacious inner world where voices of God, wise pilgrims and my own daemon sparked one another in exploration. </p>

    <p>You can use the <em>Intensive Journal</em> process to go on a journey within yourself and with the Wisdom portals that your life has brought to you. Dwell with outer life events and inner connections in order to integrate all into your authentic voice. Then you may wish to take your experiences and convictions to your EfM group to widen your awareness of your particular perspective as you listen to others’ experiences and perspectives and continually discover God’s call for your life.</p>

