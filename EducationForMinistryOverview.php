    <p id="subheading">Overview</p>

    <p>Education for Ministry (EfM) offers an in depth program of theological study that invites individuals to reflect on how their study speaks to them and calls them to action in their ministry. The Progoff&trade; Methodology is an integrated system of practical writing exercises that are conducted in a reflective atmosphere that can help to develop this self-awareness.</p>

    <p>The EfM program draws on the work of Barnard Lonergan, SJ who stressed the importance of self-appropriation (footnote). In The Self-Appropriation of Interiority: A Foundation for Psychology, Dr William Eidle stressed that self-appropriation can be achieved through the methods of Fr Lonergan and Dr Progoff. Footnote </p>

    <p>The <em>Intensive Journal</em> method can serve as an excellent resource to complement your EfM learning experience. </p>

