    <p id="endorsement">One of the most profound spiritual experiences of my life came in 1984, during a journaling retreat led by the psychotherapist Ira Progoff. At this retreat, held in Dayton, Ohio, Progoff guided us as we wrote privately for several days on some very human but ordinary questions. I remember first dialoguing with my own body, dialoguing with roads not taken, dialoguing with concrete events and persons, dialoguing with my own past decisions, on and on. </p>

    <p id="endorsement">I learned that if the quiet space, the questions themselves, and blank pages had not been put in front of me, I may never have known what was lying within me. Dr. Progoff helped me and many others access slow tears and fast prayers, and ultimately often intense happiness and gratitude, as I discovered depths within myself that I never knew were there. I still reread some of what I wrote over forty years ago for encouragement and healing. And it all came from within me!</p>

