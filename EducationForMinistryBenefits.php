    <p>The <em>Intensive Journal</em> method can serve as a valuable resource to develop your experience in the five thematic areas of the Education for Ministry Program:</p>

    <p id="subheading">Develop Your Spiritual History and Enhance Your Listening Skills</p>

    <ul>
        <li>Develop a foundation on your life as you work in many areas to develop a more meaningful life</li>
        <li>Realize connections and relationships between various areas of your life and the spiritual component.</li>
        <li>Utilize Progoff's Meaning Dimension exercises to articulate and clarify your spiritual history throughout your life</li>
        <li>Become more receptive to hearing your inner voice and become less judgmental; enhance your listening skills by increasing your ability to recognize many different facets of experience.</li>
    </ul>

    <p id="subheading">Learn Techniques to Foster Theological Reflection</p>
    <ul>
        <li>Experience a reflective and contemplative process. Apply a neutral meditative process for becoming centered and deepening your inward journey.</li>
        <li>Obtain a direct experience in your life. Benefit from a deepening workshop experience away from your daily routine to connect with your inner self.</li>
        <li>Apply Progoff's techniques to find the inner meaning for your life in the theological readings. Use exercises to access symbolic imagery and to create metaphors.</li>
    </ul>

    <p id="subheading">Construct a Spiritual Development Plan to Support Ministry</p>
    <ul>
        <li> Deepen your experience of the Sacred in your life.</li>
        <li>Discern the deep inner purpose and direction of your life as it can be lived most authentically in the world.</li>
        <li>Connect with and develop greater awareness and sensitivity to important religious/ spiritual experiences.</li>
        <li>Clarify beliefs, values, priorities, truths, purpose and religious/spiritual experiences</li>
        <li>Crystallize spiritual beliefs to formulate your plan for ministry.</li>
    </ul>

    <p id="subheading">Examine the Relationship Between Belief, Behavior and Doctrine</p>
    <ul>
        <li>Utilize the integrative aspects of the workbook structure of exercises to help you see connections between your beliefs, behavior and doctrine.</li>
        <li>Apply specific exercises such as Peaks Depths and Explorations that provide the means for more clearly realizing patterns between belief and action</li>
    </ul>

    <p id="subheading">Explore Ways to Listen to and Respond to God's Call to Ministry</p>
    <ul>
        <li>Use Progoff's technique of inner dialogue to connect with the wisdom of God and spiritual figures to obtain clarity and understanding to develop your call to ministry.</li>
        <li>Explore how your whole life is pointing towards your ministry.</li>
    </ul>




